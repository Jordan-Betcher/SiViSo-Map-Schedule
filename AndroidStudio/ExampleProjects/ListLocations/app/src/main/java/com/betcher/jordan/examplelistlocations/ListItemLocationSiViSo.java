package com.betcher.jordan.examplelistlocations;

public class ListItemLocationSiViSo {

    private String location;
    private String siviso;

    public ListItemLocationSiViSo(String location, String siviso) {
        this.location = location;
        this.siviso = siviso;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getSiviso() {
        return siviso;
    }

    public void setSiviso(String siviso) {
        this.siviso = siviso;
    }
}
